package com.example.javaproject2.week2.day3;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;
        System.out.printf("a + b = %d\n", a, b, a + b);
        System.out.printf("a - b = %d\n", a, b, a - b);
        System.out.printf("a * b = %d\n", a, b, a * b);
        System.out.printf("a / b = %d\n", a, b, a / b);
        System.out.printf("a %% b = %d\n", a, b, a % b);
    }
}
